La Sonothèque Normandie est une plateforme de streaming musical pour les œuvres Normandes.<br/>
Elle a pour vocation la mise en avant de la richesse culturelle régionale et favorise l’accès à la découverte des artistes locaux par la gratuité de ses supports. <br/> <br/>
<strong>Le projet s’oriente autour de 3 axes principaux : </strong><br/><br/>
- l’archivage des œuvres discographiques (vinyles, CD…) par la numérisation,<br/> - la diffusion des oeuvres via le site Internet et l'application pour smartphones et tablettes,<br/> - la promotion des oeuvres et des artistes.<br/>
<br/>
<strong>Un relais d’informations</strong><br/><br/>
La Sonothèque relaie les nouveautés artistiques, sorties d’albums, clips.<br/> Mais également les informations des structures musicales de la région à destination des artistes comme les tremplins, les projets d’accompagnement et tout autres dispositifs pouvant permettre aux musiciens d’évoluer professionnellement.<br/>
<br/>
<strong>Historique :</strong><br/>
Créée en 2010, la Sonothèque est le résultat d’une réflexion menée par la Fabrik à Sons sur la sauvegarde des disques régionaux de musiques actuelles depuis le passage au numérique. La Sonothèque Normandie a été l’un des premiers projets à s’intéresser à la patrimonialisation des musiques actuelles.<br/>
Depuis la création de la Sonothèque, nous avons numérisé plus de 2 800 disques de la région datant de 1962 à nos jours. L’accessibilité ce fait par le biais d’un site internet et d’une application. Des bornes d’écoute sont également déployées sur le territoire dans des établissements culturels partenaires afin de faciliter l’accès aux oeuvres.<br/><br/>
La finalité de la Sonothèque Normandie est de créer une bibliothèque musicale numérique dédiée aux productions musicales régionales.<br/><br/>
Elle est aussi de transmettre gratuitement le patrimoine normand. Par ce projet, la Sonothèque Normandie donne l'accès à des oeuvres introuvables et enrichie l’offre légale tout en valorisant la richesse culturelle de la région.<br/><br/>
Afin de poursuivre sa volonté d’être un projet d’envergure régionale, en 2016 la Sonothèque, originellement en Haute-Normandie, s’est étendue à la région Normandie. Le répertoire artistique de la Sonothèque Normandie englobe aujourd’hui le Calvados, la Manche, l’Orne, l’Eure et la Seine-Maritime.<br/>
